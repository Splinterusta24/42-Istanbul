void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	x;

	x = 0;
	i = 0;
	while (i < size)
	{
		j = 1;
		while (j <= size)
		{
			if (tab[i] > tab[i + 1])
			{
				x = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = x;
			}
			j++;
		}
		i++;
	}
}
