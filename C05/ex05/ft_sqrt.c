int	ft_sqrt(int nb)
{
	int	x;

	x = 1;
	if (nb > 0)
	{
		while (x * x <= nb && x <= 46340)
		{
			if (x * x == nb)
				return (x);
			else
				x++;
		}
		return (0);
	}
	return (0);
}
